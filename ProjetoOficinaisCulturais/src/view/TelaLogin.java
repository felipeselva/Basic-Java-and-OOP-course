    private void bntEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEntrarActionPerformed
        
    
    
    LoginController controller = new LoginController();

    String login = txtLogin.getText();
    String senha = new String(txtSenha.getPassword()); 

    boolean acessoPermitido = controller.validarLogin(login, senha);

    
    if (acessoPermitido) {
        
        TelaOficinas telaOficinas = new TelaOficinas();
        telaOficinas.setVisible(true);
        this.dispose(); 
    } else {
        
        JOptionPane.showMessageDialog(this, "Login ou senha inv√°lidos!", "Erro", JOptionPane.ERROR_MESSAGE);
    }

        
    }//GEN-LAST:event_bntEntrarActionPerformed

